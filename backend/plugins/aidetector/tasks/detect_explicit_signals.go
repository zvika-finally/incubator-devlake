// Default confidence levels (can be overridden via settings)
// These are used when settings are not yet loaded or as fallbacks
const (
	DefaultConfidenceTrailer = 95 // Git trailers - highest confidence
	DefaultConfidenceBody    = 85 // PR body/commit markers - high confidence
	DefaultConfidenceGeneric = 60 // Generic AI indicators - lower confidence
	DefaultConfidenceEmail   = 90 // Known AI bot emails
)

// AIToolPattern defines patterns for detecting a specific AI tool
type AIToolPattern struct {
	Tool       string
	Confidence int
	Patterns   []*regexp.Regexp
}

